"""
Topic: Operators in Python
"""

a = 10
b = 3

# Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Comparison Operators
print("\n--- Comparison ---")
print("Equal:", a == b)
print("Not Equal:", a != b)
print("Greater Than:", a > b)
print("Less Than:", a < b)
print("Greater or Equal:", a >= b)

# Logical Operators
x = True
y = False
print("\n--- Logical ---")
print("AND:", x and y)
print("OR:", x or y)
print("NOT:", not x)

# Assignment Operators
c = 5
c += 2
print("\nAfter += :", c)
c *= 3
print("After *= :", c)